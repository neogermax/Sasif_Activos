function transacionAjax_CargaBusqueda(State) {
    $.ajax({
        url: "CiudadesAjax.aspx",
        type: "POST",
        //crear json
        data: { "action": State,
            "tabla": 'CIUDADES'
        },
        //Transaccion Ajax en proceso
        success: function (result) {
            if (result == "") {
                ArrayCombo = [];
            }
            else {
                ArrayCombo = JSON.parse(result);
                charge_CatalogList(ArrayCombo, "DDLColumns", 1);
            }
        },
        error: function () {

        }
    });
}

